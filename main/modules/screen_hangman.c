void DisplayHangman(
    char disabled_letters[nbletters],
    char letters_found[longestword],
    int  word_length,
    int  mistake_count,
    char choosen_word[longestword]
) {
    pax_buf_t* gfx = bsp_get_gfx_buffer();
    pax_background(gfx, WHITE);
    switch (mistake_count) {
        case 1: pax_insert_png_buf(gfx, deibler01_png_start, deibler01_png_end - deibler01_png_start, 0, 0, 0); break;
        case 2: pax_insert_png_buf(gfx, deibler02_png_start, deibler02_png_end - deibler02_png_start, 0, 0, 0); break;
        case 3: pax_insert_png_buf(gfx, deibler03_png_start, deibler03_png_end - deibler03_png_start, 0, 0, 0); break;
        case 4: pax_insert_png_buf(gfx, deibler04_png_start, deibler04_png_end - deibler04_png_start, 0, 0, 0); break;
        case 5: pax_insert_png_buf(gfx, deibler05_png_start, deibler05_png_end - deibler05_png_start, 0, 0, 0); break;
        case 6: pax_insert_png_buf(gfx, deibler06_png_start, deibler06_png_end - deibler06_png_start, 0, 0, 0); break;
        case 7: pax_insert_png_buf(gfx, deibler07_png_start, deibler07_png_end - deibler07_png_start, 0, 0, 0); break;
        case 8: pax_insert_png_buf(gfx, deibler08_png_start, deibler08_png_end - deibler08_png_start, 0, 0, 0); break;
        case 9: pax_insert_png_buf(gfx, deibler09_png_start, deibler09_png_end - deibler09_png_start, 0, 0, 0); break;
        case 10: pax_insert_png_buf(gfx, deibler10_png_start, deibler10_png_end - deibler10_png_start, 0, 0, 0); break;
        default: Addborder2toBuffer(); break;
    }


    int dblock_o_x = 100;
    int dblock_o_y = 17;
    int dblock_g_x = 15;
    int letter_o_x = 2;
    int letter_o_y = 1;

    int text_origing_x = 90;
    int text_o_y       = 60;
    int text_g_x       = 14;
    int text_o_x       = text_origing_x + (290 - text_origing_x) / 2 - text_g_x * word_length / 2;
    ESP_LOGE(TAG, "text_o_x: %d", text_o_x);
    char fuckme[5] = "";

    // Draws the already tried wrong letters
    for (int i = 0; i < mistake_count; i++) {
        int y = i;
        if (mistake_count > 12) {
            y          = i - 13;
            dblock_o_y = 32;
        }
        pax_insert_png_buf(
            gfx,
            circl12_png_start,
            circl12_png_end - circl12_png_start,
            dblock_o_x + y * dblock_g_x,
            dblock_o_y,
            0
        );
        fuckme[0] = disabled_letters[y];
        pax_draw_text(
            gfx,
            BLACK,
            font1,
            fontsizeS,
            letter_o_x + dblock_o_x + y * dblock_g_x,
            letter_o_y + dblock_o_y,
            fuckme
        );
    }
    // draw the word
    ESP_LOGE(TAG, "word_length: %d", word_length);
    for (int i = 0; i < word_length; i++) {
        fuckme[0] = letters_found[i];
        ESP_LOGE(TAG, "i: %d", i);
        if (mistake_count < mistakesallowed)
            if (letters_found[i] == NULL)
                pax_center_text(gfx, BLACK, font1, fontsizeS * 2, text_o_x + text_g_x * i, text_o_y, "-");
            else
                pax_center_text(gfx, BLACK, font1, fontsizeS * 2, text_o_x + text_g_x * i, text_o_y, fuckme);
        else {
            fuckme[0] = choosen_word[i];
            pax_center_text(gfx, BLACK, font1, fontsizeS * 2, text_o_x + text_g_x * i, text_o_y, fuckme);
        }
    }
    bsp_display_flush();
}

screen_t screen_hangman_entry(QueueHandle_t application_event_queue, QueueHandle_t keyboard_event_queue) {
    bsp_apply_lut(lut_4s);

    switch (screen_hangman_splash(application_event_queue, keyboard_event_queue)) {
        case screen_HM_playing: break;
        case screen_home: return screen_home;
        default: break;
    }

    int wordtype = screen_hangman_wordtype(application_event_queue, keyboard_event_queue);

    InitKeyboard(keyboard_event_queue);
    configure_keyboard_presses(keyboard_event_queue, true, false, false, false, false);
    configure_keyboard_typing(keyboard_event_queue, true);

    char choosen_word[longestword] = "";
    char letters_found[longestword];
    for (int i = 0; i < longestword; i++) letters_found[i] = NULL;
    char disabled_letters[nbletters];
    for (int i = 0; i < nbletters; i++) disabled_letters[i] = NULL;

    int chosenwordID = 0;
    if (wordtype == victorian) {
        chosenwordID = esp_random() % sizeof(victorian_words);
        chosenwordID = 136;
        strcpy(choosen_word, victorian_words[chosenwordID]);
    } else if (wordtype == english) {
        chosenwordID = esp_random() % sizeof(english_words);
        strcpy(choosen_word, english_words[chosenwordID]);
    }
    int word_length = strlen(choosen_word);
    ESP_LOGE(TAG, "word_length: %d", word_length);
    int displayflag   = 1;
    int mistake_count = 0;

    for (int i = 0; i < nbletters; i++) disabled_letters[i] = NULL;

    while (1) {
        if (displayflag) {
            DisplayHangman(disabled_letters, letters_found, word_length, mistake_count, choosen_word);
            displayflag = 0;
            // check for victory
            if (mistake_count >= mistakesallowed) {
                vTaskDelay(pdMS_TO_TICKS(5000));
                if (wordtype == victorian)
                    screen_hangman_definition(application_event_queue, keyboard_event_queue, chosenwordID);
                return screen_hangman_victory(application_event_queue, keyboard_event_queue, 0);
            }
            int flagvictory = 1;
            for (int i = 0; i < word_length; i++)
                if (letters_found[i] == NULL)
                    flagvictory = 0;
            if (flagvictory) {
                vTaskDelay(pdMS_TO_TICKS(2000));
                if (wordtype == victorian)
                    screen_hangman_definition(application_event_queue, keyboard_event_queue, chosenwordID);
                return screen_hangman_victory(application_event_queue, keyboard_event_queue, 1);
            }
        }
        event_t event = {0};
        if ((xQueueReceive(application_event_queue, &event, pdMS_TO_TICKS(10)) == pdTRUE)) {
            switch (event.type) {
                case event_input_button: break;  // Ignore raw button input
                case event_input_keyboard:
                    switch (event.args_input_keyboard.action) {
                        case SWITCH_1:
                            if (Screen_Confirmation(forfeitprompt, application_event_queue, keyboard_event_queue))
                                InitKeyboard(keyboard_event_queue);
                            return screen_home;
                            break;
                        case SWITCH_3: break;
                        case SWITCH_4: break;
                        case SWITCH_5: break;
                        default: break;
                    }
                    if (event.args_input_keyboard.character != '\0') {
                        char input = event.args_input_keyboard.character;
                        ESP_LOGE(TAG, "Input: %c", input);
                        int flagcorrectguess = 0;
                        for (int i = 0; i < word_length; i++)
                            if (input == choosen_word[i]) {
                                letters_found[i] = input;
                                ESP_LOGE(TAG, "match location: %c", letters_found[i]);
                                ESP_LOGE(TAG, "match location: %d", i);
                                flagcorrectguess = 1;
                            }
                        ESP_LOGE(TAG, "flagcorrectguess: %d", flagcorrectguess);

                        if (!flagcorrectguess) {
                            ESP_LOGE(TAG, "inbcorrect guess");
                            ESP_LOGE(TAG, "letter nb: %d", InputtoNum(input));
                            disabled_letters[mistake_count] = input;
                            mistake_count++;
                        }
                        configure_keyboard_character(keyboard_event_queue, InputtoNum(input), false);
                        displayflag = 1;
                    }
                    break;
                default: ESP_LOGE(TAG, "Unhandled event type %u", event.type);
            }
        }
    }
}

